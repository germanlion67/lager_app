void main() async {
  final random = Random(42);

  // ==================== STAMMDATEN ====================

  final orte = [
    'Lager A', 'Lager B', 'Lager C',
    'Werkstatt', 'Büro', 'Keller',
    'Regal 1', 'Regal 2', 'Regal 3',
  ];

  final faecher = [
    'F1', 'F2', 'F3', 'F4', 'F5',
    'A1', 'A2', 'B1', 'B2', 'C1',
  ];

  final kategorien = [
    'Widerstände', 'Kondensatoren', 'Transistoren',
    'ICs', 'LEDs', 'Dioden', 'Relais',
    'Kabel', 'Stecker', 'Schalter',
    'Sensoren', 'Motoren', 'Netzteile',
  ];

  final einheiten = ['Stück', 'Meter', 'Rolle', 'Packung', 'Set'];

  // ==================== VERZEICHNISSE ====================

  final outputDir = Directory('test/performance/import_500');
  final imagesDir = Directory('test/performance/import_500/images');

  if (!outputDir.existsSync()) outputDir.createSync(recursive: true);
  if (!imagesDir.existsSync()) imagesDir.createSync(recursive: true);

  // ==================== ARTIKEL GENERIEREN ====================

  final artikelList = <Map<String, dynamic>>[];

  for (int i = 1; i <= 500; i++) {
    final kategorie = kategorien[random.nextInt(kategorien.length)];
    final einheit = einheiten[random.nextInt(einheiten.length)];
    final ort = orte[random.nextInt(orte.length)];
    final fach = faecher[random.nextInt(faecher.length)];
    final menge = random.nextInt(500) + 1;
    final bildDateiname = 'images/artikel_${i.toString().padLeft(3, '0')}.jpg';

    final now = DateTime.now().toUtc();
    final erstelltAm = now.subtract(Duration(days: random.nextInt(365)));
    final aktualisiertAm = erstelltAm.add(
      Duration(hours: random.nextInt(72)),
    );

    // ✅ Minimales JPEG pro Artikel schreiben
    final bildFile = File(
      'test/performance/import_500/$bildDateiname',
    );
    await bildFile.writeAsBytes(_minimalJpeg);

    artikelList.add({
      'name': '$kategorie ${i.toString().padLeft(3, '0')}',
      'menge': menge,
      'ort': ort,
      'fach': fach,
      'beschreibung':
          '$kategorie – $menge $einheit auf Lager. '
          'Artikel-Nr: ART-${i.toString().padLeft(5, '0')}',
      'bildPfad': bildDateiname,         // ✅ relativer Pfad
      'erstelltAm': erstelltAm.toIso8601String(),
      'aktualisiertAm': aktualisiertAm.toIso8601String(),
      'deleted': false,
    });
  }

  // ==================== import.json SPEICHERN ====================

  final jsonFile = File('test/performance/import_500/import.json');
  await jsonFile.writeAsString(
    const JsonEncoder.withIndent('  ').convert(artikelList),
  );
  stdout.writeln(
    '✅ import.json erstellt: ${artikelList.length} Artikel',
  );

  // ==================== CSV SPEICHERN ====================

  final csvBuffer = StringBuffer();
  csvBuffer.writeln(
    'name,menge,ort,fach,beschreibung,bildPfad,'
    'erstelltAm,aktualisiertAm,deleted',
  );

  for (final a in artikelList) {
    String esc(dynamic v) {
      final s = v.toString();
      return (s.contains(',') || s.contains('"') || s.contains('\n'))
          ? '"${s.replaceAll('"', '""')}"'
          : s;
    }

    csvBuffer.writeln([
      esc(a['name']),
      esc(a['menge']),
      esc(a['ort']),
      esc(a['fach']),
      esc(a['beschreibung']),
      esc(a['bildPfad']),
      esc(a['erstelltAm']),
      esc(a['aktualisiertAm']),
      esc(a['deleted']),
    ].join(','),);
  }

  final csvFile = File('test/performance/import_500/artikel_500.csv');
  await csvFile.writeAsString(csvBuffer.toString());
  stdout.writeln('✅ artikel_500.csv erstellt');

  stdout.writeln(
    '✅ images/ erstellt: 500 JPEG-Platzhalter\n'
    '🎉 Dataset fertig unter: test/performance/import_500/',
  );
}